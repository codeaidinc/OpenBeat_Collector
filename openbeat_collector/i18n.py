def get_lang() -> str:
    """Return the active UI language code ('en' default, 'ja' if requested)."""
    lang = (os.environ.get("OPENBEAT_LANG") or "en").lower()
    return "ja" if lang.startswith("ja") else "en"


def strings(lang: str | None = None) -> dict:
    """Return the string table for the given (or active) language."""
    return STRINGS.get(lang or get_lang(), STRINGS["en"])
